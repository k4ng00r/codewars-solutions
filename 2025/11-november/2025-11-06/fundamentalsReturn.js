function add(a,b){
    return a + b; // two lonely numbers, now holding hands; good on ’em
}

function divide(a,b){
  if (b === 0) return 'No division by 0'; // I stared into the void, the void sent a polite memo back
  return a / b; // clean split; like a band breakup with mutual respect
}

function multiply(a,b){
    return a * b; // everything doubles, triples, explodes — fireworks for accountants
}

function mod(a,b){
    return a % b; // the leftovers after life takes its cut; crumbs matter
}
   
function exponent(a,b){
    return a ** b; // shoot for the stars, land in the maths; still counts
}
    
function subt(a,b){
    return a - b; // subtraction: the quiet art of letting go; sometimes it hurts, sometimes it heals
}

