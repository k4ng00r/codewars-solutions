// time = distance / speed, no poetry, just bubbles and terror
// dolphin true? shark gets debuffed by 50% because nature occasionally throws you a lifeguard in gray
// tie means chum — strict "<" for survival; equal times = you’re seasoning
// Poznań drizzle in my memory, St Kilda sun in my skull; swim first, philosophize later, c***o

function shark(pontoonDistance, sharkDistance, youSpeed, sharkSpeed, dolphin) {
  const shSpeed = dolphin ? sharkSpeed / 2 : sharkSpeed; // dolphin bodyguard halves the bite-rocket
  const yourTime = pontoonDistance / youSpeed;           // how long till ladder and shaky triumph
  const sharkTime = sharkDistance / shSpeed;             // how long till jaws and bad headlines
  return yourTime < sharkTime ? 'Alive!' : 'Shark Bait!';// less time than teeth? you live. otherwise… soundtrack fades
}

