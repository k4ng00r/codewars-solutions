function getGrade (s1, s2, s3) {
  const score = (s1 + s2 + s3) / 3;
  // Average has that Monday energy: not spectacular, not tragic, just definitive.

  if (score >= 90 && score <= 100) return 'A';       // rare sunshine, Melbourne-level sky
  else if (score >= 80 && score < 90) return 'B';    // strong breeze, no waves
  else if (score >= 70 && score < 80) return 'C';    // pass the class, promise to revise
  else if (score >= 60 && score < 70) return 'D';    // coffee and consequences
  else if (score >= 0 && score < 60) return 'F';     // night met morning; morning kept the receipt (╯°□°）╯︵ ┻━┻
}

