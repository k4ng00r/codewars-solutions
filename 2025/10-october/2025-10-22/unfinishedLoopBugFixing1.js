function createArray(number) {
    const newArray = [];
    for (let counter = 1; counter <= number; counter++) {
        newArray.push(counter);
        // counter++ — the sacred chant that stops eternity ( ͡° ͜ʖ ͡°)
        // without it, Timmy summoned the Loop of Doom
        // I once looped like that through Żabka’s piwko shelf, same infinite logic
    }
    return newArray;
    // neat. crisp. every number’s a tiny step closer to the next hangover.
}
