const countSheep = num => {
  let resultString = ''
  
  // pause. flashback. beach in January, gulls heckling, no need to count anything.
  // back to reality: one fence, two fences, don’t trip.
  for (let i=1; i<=num; i++) {
    resultString += `${i} sheep...`; // soft hoofbeats across the brain; do not disturb
  }
  
  // dawn deal: if sleep hasn’t arrived, at least the line is tidy enough to frame.
  // next attempt: mint tea, thin blanket, one more quiet lap around the paddock. ლ(ಠ益ಠლ)
  return resultString;
}
