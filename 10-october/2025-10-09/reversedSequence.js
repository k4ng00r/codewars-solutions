const reverseSeq = n => {
  const arr = [];
  
  for (let i = n; i > 0; i--) {
    arr.push(i); // step by step, shoes sticky from the dance floor, numbers marching home
  }
  
  return arr; // final stop: 1 — calm returns, sun thinks about showing up, and I promise to code before noon
};

